    private double wyborPrzelicznikaZlewej(){
        double wybranyPrzelicznikLewy = 1.0f;
        if(naZłoteWprawo.isSelected()) wybranyPrzelicznikLewy = Zloty;
        else if(naEuroWprawo.isSelected()) wybranyPrzelicznikLewy = Euro;
        else if(naDolaryWprawo.isSelected()) wybranyPrzelicznikLewy = Dolar;
        else if(naRubleWprawo.isSelected()) wybranyPrzelicznikLewy = Rubel;
        return wybranyPrzelicznikLewy;
    }
    
        private double wyborPrzelicznikaZprawej(){
        double wybranyPrzelicznikPrawy = 1.0f;
        if(naZłoteWlewo.isSelected()) wybranyPrzelicznikPrawy = Zloty;
        else if(naEuroWlewo.isSelected()) wybranyPrzelicznikPrawy = Euro;
        else if(naDolaryWlewo.isSelected()) wybranyPrzelicznikPrawy = Dolar;
        else if(naRubleWlewo.isSelected()) wybranyPrzelicznikPrawy = Rubel;
        return wybranyPrzelicznikPrawy;
    }
    
    
