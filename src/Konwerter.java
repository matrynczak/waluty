    private void przeliczWprawoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_przeliczWprawoActionPerformed
        
        double wybranyPrzelicznik = wyborPrzelicznikaZprawej()/wyborPrzelicznikaZlewej();
        String kwota_str = wejscie.getText();
        if(kwota_str.equals("")){
            wyjscie.setText("Nic nie wpisano");
        }
        else {
            try{
                DecimalFormat df = new DecimalFormat("0.00");
                double kwota = df.parse(kwota_str).doubleValue();
                wyjscie.setText(""+df.format(kwota/wybranyPrzelicznik));
            }catch(NumberFormatException ex){
                wyjscie.setText("DUPA");
            } catch (ParseException ex) {
                Logger.getLogger(Konwerter.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_przeliczWprawoActionPerformed

